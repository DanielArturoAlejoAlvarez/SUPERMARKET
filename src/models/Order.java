package models;



/**
 *
 * @author daniel
 */
public class Order {
    
    int id;
    int idClient;
    int iduser;
    String serialNumber;
    String date;
    double total;
    String flag;
    
    public Order() {
    
    }

    public Order(int id, int idClient, int iduser, String serialNumber, String date, double total, String flag) {
        this.id = id;
        this.idClient = idClient;
        this.iduser = iduser;
        this.serialNumber = serialNumber;
        this.date = date;
        this.total = total;
        this.flag = flag;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdClient() {
        return idClient;
    }

    public void setIdClient(int idClient) {
        this.idClient = idClient;
    }

    public int getIduser() {
        return iduser;
    }

    public void setIduser(int iduser) {
        this.iduser = iduser;
    }

    public String getSerialNumber() {
        return serialNumber;
    }

    public void setSerialNumber(String serialNumber) {
        this.serialNumber = serialNumber;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }

    public String getFlag() {
        return flag;
    }

    public void setFlag(String flag) {
        this.flag = flag;
    }
    
    
}

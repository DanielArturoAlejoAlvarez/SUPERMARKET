package models;

/**
 *
 * @author daniel
 */
public class Client {
    int id;
    String dni;
    String displayName;
    String address;
    String flag;
    
    public Client() {
        
    }

    public Client(int id, String dni, String displayName, String address, String flag) {
        this.id = id;
        this.dni = dni;
        this.displayName = displayName;
        this.address = address;
        this.flag = flag;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getDisplayName() {
        return displayName;
    }

    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getFlag() {
        return flag;
    }

    public void setFlag(String flag) {
        this.flag = flag;
    }
    
        
    
}

    void listUser() {
        List<User> list = udao.list();
        model = (DefaultTableModel) tbUser.getModel();
        Object[] obj = new Object[6];
        for (int i = 0; i < list.size(); i++) {
            obj[0] = list.get(i).getId();
            obj[1] = list.get(i).getDni();
            obj[2] = list.get(i).getDisplayName();
            obj[3] = list.get(i).getUsername();
            obj[4] = list.get(i).getPhone();
            obj[5] = list.get(i).getFlag();
            model.addRow(obj);
        }
        tbUser.setModel(model);
    }

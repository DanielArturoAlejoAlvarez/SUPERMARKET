    private void tbUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbUserMouseClicked
        int row = tbUser.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "You must select a row");
        }else {
            id = Integer.parseInt(tbUser.getValueAt(row, 0).toString());
            String dni = tbUser.getValueAt(row, 1).toString();
            String names = tbUser.getValueAt(row, 2).toString();
            String username = tbUser.getValueAt(row, 3).toString();
            String phone = tbUser.getValueAt(row, 4).toString();
            String status = tbUser.getValueAt(row, 5).toString();
            
            txtDni.setText(dni);
            txtNames.setText(names);
            txtUsername.setText(username);
            txtPhone.setText(phone);
            cboStatus.setSelectedItem(status);
        }
    }//GEN-LAST:event_tbUserMouseClicked


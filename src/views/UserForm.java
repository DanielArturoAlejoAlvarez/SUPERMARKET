    void addUser() {
        String dni = txtDni.getText();
        String names = txtNames.getText();
        String username = txtUsername.getText();
        String phone = txtPhone.getText();
        String status = cboStatus.getSelectedItem().toString();
        
        Object[] obj = new Object[5];
        
        obj[0] = dni;
        obj[1] = names;
        obj[2] = username;
        obj[3] = phone;
        obj[4] = status;
        
        udao.add(obj);
        
    }
    
    void updateUser() {
        int row = tbUser.getSelectedRow();
        if(row == -1) {
            JOptionPane.showMessageDialog(this, "You must select a row");
        }else {
            String dni = txtDni.getText();
            String names = txtNames.getText();
            String username = txtUsername.getText();
            String phone = txtPhone.getText();
            String status = cboStatus.getSelectedItem().toString();

            Object[] obj = new Object[6];

            obj[0] = dni;
            obj[1] = names;
            obj[2] = username;
            obj[3] = phone;
            obj[4] = status;
            obj[5] = id;

            udao.update(obj);
        }        
    }
    
    void deleteUser() {
        int row = tbUser.getSelectedRow();
        int idUser = Integer.parseInt(tbUser.getValueAt(row, 0).toString());
        if(row == -1) {
            JOptionPane.showMessageDialog(this, "You must select a row");
        }else{
            udao.delete(idUser);
        }
    }
    
    void resetUser() {
        txtDni.setText("");
        txtNames.setText("");
        txtUsername.setText("");
        txtPhone.setText("");
        cboStatus.setSelectedItem("");
        txtDni.requestFocus();
    }
    
    void clearForm() {
        for (int i = 0; i < model.getRowCount(); i++) {
            model.removeRow(i);
            i = i-1;
        }
    }
    


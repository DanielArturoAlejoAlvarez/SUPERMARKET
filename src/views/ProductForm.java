    void listProduct() {
        List<Product> list = pdao.list();
        model = (DefaultTableModel) tbProduct.getModel();
        Object[] obj = new Object[5];
        for (int i = 0; i < list.size(); i++) {
            obj[0] = list.get(i).getId();
            obj[1] = list.get(i).getName();
            obj[2] = list.get(i).getPrice();
            obj[3] = list.get(i).getStock();
            obj[4] = list.get(i).getFlag();
            
            model.addRow(obj);
        }
        
        tbProduct.setModel(model);
    }

    void addProduct() {
        String name = txtName.getText();
        double price = Double.parseDouble(txtPrice.getText());
        int stock = Integer.parseInt(txtStock.getText());
        String status = cboStatus.getSelectedItem().toString();
        
        Object[] obj = new Object[4];
        obj[0] = name;
        obj[1] = price;
        obj[2] = stock;
        obj[3] = status;
        
        pdao.add(obj);
    }
    
    void updateProduct() {
        int row = tbProduct.getSelectedRow();
        if(row == -1) {
            JOptionPane.showMessageDialog(this, "You must select a row");
        }else {
            String name = txtName.getText();
            double price = Double.parseDouble(txtPrice.getText());
            int stock = Integer.parseInt(txtStock.getText());
            String status = cboStatus.getSelectedItem().toString();

            Object[] obj = new Object[4];
            obj[0] = name;
            obj[1] = price;
            obj[2] = stock;
            obj[3] = status;
            obj[4] = id;
        
            pdao.update(obj);
        }
    }
    
    void deleteProduct() {
        int row = tbProduct.getSelectedRow();
        int idProduct = Integer.parseInt(tbProduct.getValueAt(row, 0).toString());
        if(row == -1) {
            JOptionPane.showMessageDialog(this, "You must select a row");
        }else {
            pdao.delete(idProduct);
        }
    }
    
    private void tbProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbProductMouseClicked
        int row = tbProduct.getSelectedRow();
        if(row == -1) {
            JOptionPane.showMessageDialog(this, "You must select a row");
        }else {
            id = Integer.parseInt(tbProduct.getValueAt(row, 0).toString());
            String name = tbProduct.getValueAt(row, 1).toString();
            String price = tbProduct.getValueAt(row, 2).toString();
            String stock = tbProduct.getValueAt(row, 3).toString();
            String status = tbProduct.getValueAt(row, 4).toString();
            
            txtName.setText(name);
            txtPrice.setText(price);
            txtStock.setText(stock);
            cboStatus.setSelectedItem(status);
            
        }
    }//GEN-LAST:event_tbProductMouseClicked

    void clearForm() {
        for (int i = 0; i < model.getRowCount(); i++) {
            model.removeRow(i);
            i=i-1;
        }
    }


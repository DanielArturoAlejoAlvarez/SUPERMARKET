    private void tbClientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbClientMouseClicked
        int row = tbClient.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "You must select a row");
        }else {
            id = Integer.parseInt(tbClient.getValueAt(row, 0).toString());
            String dni = tbClient.getValueAt(row, 1).toString();
            String names = tbClient.getValueAt(row, 2).toString();
            String address = tbClient.getValueAt(row, 3).toString();
            String status = tbClient.getValueAt(row, 4).toString();
            
            txtDni.setText(dni);
            txtNames.setText(names);
            txtAddress.setText(address);
            cboStatus.setSelectedItem(status);
        }
    }//GEN-LAST:event_tbClientMouseClicked

    void addClient() {
        String dni = txtDni.getText();
        String names = txtNames.getText();
        String address = txtAddress.getText();
        String status = cboStatus.getSelectedItem().toString();
        
        Object[] obj = new Object[4];
        obj[0] = dni;
        obj[1] = names;
        obj[2] = address;
        obj[3] = status;
        
        cdao.add(obj);
    }
    
    void updateClient() {
        
        int row = tbClient.getSelectedRow();
        if(row == -1) {
            JOptionPane.showMessageDialog(this, "You must select a row");
        }else {
            String dni = txtDni.getText();
            String names = txtNames.getText();
            String address = txtAddress.getText();
            String status = cboStatus.getSelectedItem().toString();

            Object[] obj = new Object[5];
            obj[0] = dni;
            obj[1] = names;
            obj[2] = address;
            obj[3] = status;
            obj[4] = id;

            cdao.update(obj);
        }        
        
    }
    
    void deleteClient() {
        int row = tbClient.getSelectedRow();
        int idClient = Integer.parseInt(tbClient.getValueAt(row, 0).toString());
        if(row == -1) {
            JOptionPane.showMessageDialog(this, "You must select a row");
        }else {
            cdao.delete(idClient);
        }    
    }
    
    void resetClient() {
        txtDni.setText("");
        txtNames.setText("");
        txtAddress.setText("");
        cboStatus.setSelectedItem("");
        txtDni.requestFocus();
    }
    
    void clearForm() {
        for (int i = 0; i < model.getRowCount(); i++) {
            model.removeRow(i);
            i = i-1;
        }
    }


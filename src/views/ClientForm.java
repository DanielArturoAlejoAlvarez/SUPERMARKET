    void listClient() {
        List<Client> list = cdao.list();
        model = (DefaultTableModel) tbClient.getModel();
        Object[] obj = new Object[5];
        for (int i = 0; i < list.size(); i++) {
            obj[0] = list.get(i).getId();
            obj[1] = list.get(i).getDni();
            obj[2] = list.get(i).getDisplayName();
            obj[3] = list.get(i).getAddress();
            obj[4] = list.get(i).getFlag();
            model.addRow(obj);
        }
        tbClient.setModel(model);
    }
